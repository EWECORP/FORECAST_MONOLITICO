				WHEN 11 THEN PRC.I_COSTO_ESTADISTICO_11
				WHEN 12 THEN PRC.I_COSTO_ESTADISTICO_12
				WHEN 13 THEN PRC.I_COSTO_ESTADISTICO_13
				WHEN 14 THEN PRC.I_COSTO_ESTADISTICO_14
				WHEN 15 THEN PRC.I_COSTO_ESTADISTICO_15
				WHEN 16 THEN PRC.I_COSTO_ESTADISTICO_16
				WHEN 17 THEN PRC.I_COSTO_ESTADISTICO_17
				WHEN 18 THEN PRC.I_COSTO_ESTADISTICO_18
				WHEN 19 THEN PRC.I_COSTO_ESTADISTICO_19
				WHEN 20 THEN PRC.I_COSTO_ESTADISTICO_20
				WHEN 21 THEN PRC.I_COSTO_ESTADISTICO_21
				WHEN 22 THEN PRC.I_COSTO_ESTADISTICO_22
				WHEN 23 THEN PRC.I_COSTO_ESTADISTICO_23
				WHEN 24 THEN PRC.I_COSTO_ESTADISTICO_24
				WHEN 25 THEN PRC.I_COSTO_ESTADISTICO_25
				WHEN 26 THEN PRC.I_COSTO_ESTADISTICO_26
				WHEN 27 THEN PRC.I_COSTO_ESTADISTICO_27
				WHEN 28 THEN PRC.I_COSTO_ESTADISTICO_28
				WHEN 29 THEN PRC.I_COSTO_ESTADISTICO_29
				WHEN 30 THEN PRC.I_COSTO_ESTADISTICO_30
				WHEN 31 THEN PRC.I_COSTO_ESTADISTICO_31
				ELSE NULL
			END AS I_COSTO_ESTADISTICO,
			CASE DAY(OC.F_EMISION)
				WHEN 1 THEN PRC.I_COSTO_PP_1
				WHEN 2 THEN PRC.I_COSTO_PP_2
				WHEN 3 THEN PRC.I_COSTO_PP_3
				WHEN 4 THEN PRC.I_COSTO_PP_4
				WHEN 5 THEN PRC.I_COSTO_PP_5
				WHEN 6 THEN PRC.I_COSTO_PP_6
				WHEN 7 THEN PRC.I_COSTO_PP_7
				WHEN 8 THEN PRC.I_COSTO_PP_8
				WHEN 9 THEN PRC.I_COSTO_PP_9
				WHEN 10 THEN PRC.I_COSTO_PP_10
				WHEN 11 THEN PRC.I_COSTO_PP_11
				WHEN 12 THEN PRC.I_COSTO_PP_12
				WHEN 13 THEN PRC.I_COSTO_PP_13
				WHEN 14 THEN PRC.I_COSTO_PP_14
				WHEN 15 THEN PRC.I_COSTO_PP_15
				WHEN 16 THEN PRC.I_COSTO_PP_16
				WHEN 17 THEN PRC.I_COSTO_PP_17
				WHEN 18 THEN PRC.I_COSTO_PP_18
				WHEN 19 THEN PRC.I_COSTO_PP_19
				WHEN 20 THEN PRC.I_COSTO_PP_20
				WHEN 21 THEN PRC.I_COSTO_PP_21
				WHEN 22 THEN PRC.I_COSTO_PP_22
