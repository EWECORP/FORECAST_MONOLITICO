CREATE PROCEDURE [dbo].[SP_CNX_T_OC_DETALLE_EXT]
		@FechaDesde datetime = NULL

AS
	SET NOCOUNT ON;
	-- Validar Parámetro
	IF @FechaDesde IS NULL  
	BEGIN  
		PRINT 'ERROR: Debe especifiarse la fecha desde en el Parámetro Ej.20250101'
		RETURN  
	END

BEGIN    

	/******VOLVER A GENERAR TABLA  ******/
	--- SET @FechaDesde='20241101'
	IF  EXISTS (SELECT * FROM sys.objects WHERE object_id = OBJECT_ID(N'[dbo].[T_OC_DETALLE_EXT]') AND type in (N'U'))
	TRUNCATE TABLE [dbo].[T_OC_DETALLE_EXT]

	INSERT INTO [dbo].[T_OC_DETALLE_EXT]
		([C_OC],[F_EMISION],[U_PREFIJO_OC],[U_SUFIJO_OC],[C_PROVEEDOR],[C_SUCU_COMPRA],[C_SUCU_DESTINO],[C_ARTICULO]
		,[Q_BULTOS_SUGERIDOS],[Q_BULTOS_PROV_PED],[Q_FACTOR_PROV_PED],[Q_BULTOS_PROV_BONIF],[Q_BULTOS_EMPR_PED],[Q_FACTOR_EMPR_PED],[Q_PESO_UNIT_ART]
		,[Q_PESO_TOTAL_PED],[Q_PESO_TOTAL_BONIF],[C_IVA_EN_CALCULO],[K_COEF_IVA],[K_IMP_INTERNO],[I_COSTO_BASE],[I_PRECIO_COMPRA]
		,[I_PRECIO_PARTE],[I_PRECIO_LISTA],[I_IMP_INTERNO],[I_ENVASES],[I_TOTAL_IMP_INTERNO],[I_TOTAL_ITEM]
		,[Q_UNID_CUMPLIDAS],[Q_PESO_CUMPLIDO],[M_CUMPLIDA_PARCIAL],[C_USUARIO_CUMPLIO_PARCIAL],[F_CUMPLIDA_PARCIAL],[U_PISO_PALETIZADO_OC],[U_ALTURA_PALETIZADO_OC]
--		,[C_DTO1_COMP],[K_DTO1_COMP],[C_DTO2_COMP],[K_DTO2_COMP],[C_DTO3_COMP],[K_DTO3_COMP],[C_DTO4_COMP],[K_DTO4_COMP]
--		,[C_DTO5_COMP],[K_DTO5_COMP],[C_DTO6_COMP],[K_DTO6_COMP],[C_DTO7_COMP],[K_DTO7_COMP],[C_DTO8_COMP]
--		,[K_DTO8_COMP],[C_DTO9_COMP],[K_DTO9_COMP],[C_DTO10_COMP],[K_DTO10_COMP]
		,[I_PRECIO_VTA]	,[I_COSTO_ESTADISTICO],[I_COSTO_PP]
		,[Q_VENTA_DIARIA],[Q_VENTA_MENSUAL],[Q_DIAS_STOCK],[Q_DIAS_ENTREGA_PROVEEDOR],[STOCK_UNIDADES]
		,[STOCK_VALORIZADO],[PRECIO_COMPETENCIA],[OBSERVACIONES],[N_ARTICULO]
		,[PEDIDO_UNIDADES],[PENDIENTE_UNIDADES],[ENTREGAS_PENDIENTES])

		SELECT C.[C_OC],C.[F_EMISION],C.[U_PREFIJO_OC],C.[U_SUFIJO_OC],C.[C_PROVEEDOR],C.[C_SUCU_COMPRA],C.[C_SUCU_DESTINO],D.[C_ARTICULO]
