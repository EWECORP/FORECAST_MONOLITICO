				WHEN 28 THEN PRC.I_COSTO_PP_28
				WHEN 29 THEN PRC.I_COSTO_PP_29
				WHEN 30 THEN PRC.I_COSTO_PP_30
				WHEN 31 THEN PRC.I_COSTO_PP_31
				ELSE NULL
			END AS I_COSTO_PP
		FROM [dbo].[T_OC_DETALLE_EXT] AS OC
		INNER JOIN [DIARCOP001].[DiarcoP].[dbo].[T710_ESTADIS_PRECIOS] AS PRC
			ON OC.C_ARTICULO = PRC.C_ARTICULO
			AND OC.C_SUCU_DESTINO = PRC.C_SUCU_EMPR
			AND YEAR(OC.F_EMISION) = PRC.C_ANIO
			AND MONTH(OC.F_EMISION) = PRC.C_MES
	) AS Source
	ON Target.C_ARTICULO = Source.C_ARTICULO
	AND Target.C_SUCU_DESTINO = Source.C_SUCU_DESTINO
	AND Target.U_PREFIJO_OC = Source.U_PREFIJO_OC
	AND Target.U_SUFIJO_OC = Source.U_SUFIJO_OC
	WHEN MATCHED THEN
		UPDATE SET 
			Target.I_PRECIO_VTA = Source.I_PRECIO_VTA,
			Target.I_COSTO_ESTADISTICO = Source.I_COSTO_ESTADISTICO,
			Target.I_COSTO_PP = Source.I_COSTO_PP;

END;
