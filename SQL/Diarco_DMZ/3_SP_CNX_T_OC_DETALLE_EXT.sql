				WHEN DAY(OC.F_EMISION) = 6 THEN STOCK.Q_DIA6
				WHEN DAY(OC.F_EMISION) = 7 THEN STOCK.Q_DIA7
				WHEN DAY(OC.F_EMISION) = 8 THEN STOCK.Q_DIA8
				WHEN DAY(OC.F_EMISION) = 9 THEN STOCK.Q_DIA9
				WHEN DAY(OC.F_EMISION) = 10 THEN STOCK.Q_DIA10
				WHEN DAY(OC.F_EMISION) = 11 THEN STOCK.Q_DIA11
				WHEN DAY(OC.F_EMISION) = 12 THEN STOCK.Q_DIA12
				WHEN DAY(OC.F_EMISION) = 13 THEN STOCK.Q_DIA13
				WHEN DAY(OC.F_EMISION) = 14 THEN STOCK.Q_DIA14
				WHEN DAY(OC.F_EMISION) = 15 THEN STOCK.Q_DIA15
				WHEN DAY(OC.F_EMISION) = 16 THEN STOCK.Q_DIA16
				WHEN DAY(OC.F_EMISION) = 17 THEN STOCK.Q_DIA17
				WHEN DAY(OC.F_EMISION) = 18 THEN STOCK.Q_DIA18
				WHEN DAY(OC.F_EMISION) = 19 THEN STOCK.Q_DIA19
				WHEN DAY(OC.F_EMISION) = 20 THEN STOCK.Q_DIA20
				WHEN DAY(OC.F_EMISION) = 21 THEN STOCK.Q_DIA21
				WHEN DAY(OC.F_EMISION) = 22 THEN STOCK.Q_DIA22
				WHEN DAY(OC.F_EMISION) = 23 THEN STOCK.Q_DIA23
				WHEN DAY(OC.F_EMISION) = 24 THEN STOCK.Q_DIA24
				WHEN DAY(OC.F_EMISION) = 25 THEN STOCK.Q_DIA25
				WHEN DAY(OC.F_EMISION) = 26 THEN STOCK.Q_DIA26
				WHEN DAY(OC.F_EMISION) = 27 THEN STOCK.Q_DIA27
				WHEN DAY(OC.F_EMISION) = 28 THEN STOCK.Q_DIA28
				WHEN DAY(OC.F_EMISION) = 29 THEN STOCK.Q_DIA29
				WHEN DAY(OC.F_EMISION) = 30 THEN STOCK.Q_DIA30
				WHEN DAY(OC.F_EMISION) = 31 THEN STOCK.Q_DIA31
				ELSE NULL
				END
	FROM [dbo].[T_OC_DETALLE]AS OC

	INNER JOIN [DIARCOP001].[DiarcoP].[dbo].[T710_ESTADIS_STOCK] AS STOCK
		ON OC.C_ARTICULO = STOCK.C_ARTICULO
		AND OC.[C_SUCU_DESTINO] = STOCK.C_SUCU_EMPR
		AND YEAR(OC.F_EMISION) = STOCK.C_ANIO
		AND MONTH(OC.F_EMISION) = STOCK.C_MES;

-- Actualizar  STOCK VALORIZADO
	UPDATE DET
	SET DET.[STOCK_VALORIZADO] = DET.[STOCK_UNIDADES] * DET.[I_COSTO_BASE]
	FROM [dbo].[T_OC_DETALLE]AS DET

-- Actualizar la tabla OC_DETALLE con el ESTADÍSTICA PRECIOS
	MERGE INTO [dbo].[T_OC_DETALLE_EXT] AS Target
	USING (
		SELECT 
			OC.C_ARTICULO,
			OC.C_SUCU_DESTINO,
			OC.U_PREFIJO_OC,
			OC.U_SUFIJO_OC,
			OC.F_EMISION,
			CASE DAY(OC.F_EMISION)
				WHEN 1 THEN PRC.I_PRECIO_VTA_1
				WHEN 2 THEN PRC.I_PRECIO_VTA_2
				WHEN 3 THEN PRC.I_PRECIO_VTA_3
				WHEN 4 THEN PRC.I_PRECIO_VTA_4
				WHEN 5 THEN PRC.I_PRECIO_VTA_5
				WHEN 6 THEN PRC.I_PRECIO_VTA_6
				WHEN 7 THEN PRC.I_PRECIO_VTA_7
				WHEN 8 THEN PRC.I_PRECIO_VTA_8
				WHEN 9 THEN PRC.I_PRECIO_VTA_9
				WHEN 10 THEN PRC.I_PRECIO_VTA_10
				WHEN 11 THEN PRC.I_PRECIO_VTA_11
				WHEN 12 THEN PRC.I_PRECIO_VTA_12
				WHEN 13 THEN PRC.I_PRECIO_VTA_13
				WHEN 14 THEN PRC.I_PRECIO_VTA_14
				WHEN 15 THEN PRC.I_PRECIO_VTA_15
				WHEN 16 THEN PRC.I_PRECIO_VTA_16
				WHEN 17 THEN PRC.I_PRECIO_VTA_17
				WHEN 18 THEN PRC.I_PRECIO_VTA_18
				WHEN 19 THEN PRC.I_PRECIO_VTA_19
				WHEN 20 THEN PRC.I_PRECIO_VTA_20
				WHEN 21 THEN PRC.I_PRECIO_VTA_21
				WHEN 22 THEN PRC.I_PRECIO_VTA_22
				WHEN 23 THEN PRC.I_PRECIO_VTA_23
				WHEN 24 THEN PRC.I_PRECIO_VTA_24
				WHEN 25 THEN PRC.I_PRECIO_VTA_25
				WHEN 26 THEN PRC.I_PRECIO_VTA_26
				WHEN 27 THEN PRC.I_PRECIO_VTA_27
				WHEN 28 THEN PRC.I_PRECIO_VTA_28
				WHEN 29 THEN PRC.I_PRECIO_VTA_29
				WHEN 30 THEN PRC.I_PRECIO_VTA_30
				WHEN 31 THEN PRC.I_PRECIO_VTA_31
				ELSE NULL
			END AS I_PRECIO_VTA,
			CASE DAY(OC.F_EMISION)
				WHEN 1 THEN PRC.I_COSTO_ESTADISTICO_1
				WHEN 2 THEN PRC.I_COSTO_ESTADISTICO_2
				WHEN 3 THEN PRC.I_COSTO_ESTADISTICO_3
				WHEN 4 THEN PRC.I_COSTO_ESTADISTICO_4
				WHEN 5 THEN PRC.I_COSTO_ESTADISTICO_5
