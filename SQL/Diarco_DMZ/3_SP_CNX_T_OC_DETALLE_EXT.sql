			  ,D.[Q_PESO_TOTAL_PED],D.[Q_PESO_TOTAL_BONIF],D.[C_IVA_EN_CALCULO],D.[K_COEF_IVA],D.[K_IMP_INTERNO],D.[I_COSTO_BASE],D.[I_PRECIO_COMPRA]
			  ,D.[I_PRECIO_PARTE],D.[I_PRECIO_LISTA],D.[I_IMP_INTERNO],D.[I_ENVASES],D.[I_TOTAL_IMP_INTERNO],D.[I_TOTAL_ITEM]
			  ,D.[Q_UNID_CUMPLIDAS],D.[Q_PESO_CUMPLIDO],D.[M_CUMPLIDA_PARCIAL],D.[C_USUARIO_CUMPLIO_PARCIAL],D.[F_CUMPLIDA_PARCIAL],D.[U_PISO_PALETIZADO_OC],D.[U_ALTURA_PALETIZADO_OC]
			  
			  ---- Preparo campos que voy a completar en pasos posteriores
			  ,0.00 AS I_PRECIO_VTA
			  ,0.00 AS I_COSTO_ESTADISTICO
			  ,0.00 AS I_COSTO_PP
			  ---- Preparo campos que voy a completar en pasos posteriores
			  ,0.00 AS Q_VENTA_DIARIA
			  ,0.00 AS Q_VENTA_MENSUAL
			  ,0.00 AS Q_DIAS_STOCK
			  ,0.00 AS Q_DIAS_ENTREGA_PROVEEDOR
			  ,0.000 AS STOCK_UNIDADES
				--- Adicionales
			  ,0.00 AS STOCK_VALORIZADO
			  ,0.00 AS PRECIO_COMPETENCIA
			  ,'Observaciones' AS OBSERVACIONES
			  ,'Articulo' AS N_ARTICULO
			  --- Nuevos Parámetros
			  ,ROUND(D.[Q_BULTOS_EMPR_PED] * D.[Q_FACTOR_EMPR_PED],2) AS PEDIDO_UNIDADES		
			  ,ROUND((D.[Q_BULTOS_EMPR_PED] * D.[Q_FACTOR_EMPR_PED])-D.[Q_UNID_CUMPLIDAS],2) AS PENDIENTE_UNIDADES
			  ,0.00 AS ENTREGAS_PENDIENTES

		  FROM [DIARCOP001].[DiarcoP].[dbo].[T080_OC_CABE] C
			  LEFT JOIN [DIARCOP001].[DiarcoP].[dbo].[T081_OC_DETA] D
			  ON C.[C_OC]= D.[C_OC] AND
				  C.[U_PREFIJO_OC] = D.[U_PREFIJO_OC] AND
				  C.[U_SUFIJO_OC] = D.[U_SUFIJO_OC]
			WHERE C.[F_ALTA_SIST] >@FechaDesde
			  ORDER BY [C_OC]
			  ,[U_PREFIJO_OC]
			  ,[U_SUFIJO_OC]
		
--- DESDE ESTADISTICA DE VENTAS ---
	UPDATE T
	SET T.[Q_VENTA_DIARIA] = [Q_VENTA_DIARIA_NORMAL],
			T.[Q_VENTA_MENSUAL] = (M.[Q_VENTA_30_DIAS] + M.[Q_VENTA_15_DIAS]),
		T.[Q_DIAS_STOCK] = M.[Q_DIAS_STOCK],
		T.[Q_DIAS_ENTREGA_PROVEEDOR] = M.[Q_DIAS_ENTREGA_PROVEEDOR]
	FROM [dbo].[T_OC_DETALLE] T
	INNER JOIN [DIARCOP001].[DiarcoP].[dbo].[T710_ESTADIS_REPOSICION] M
		ON T.[C_SUCU_COMPRA] = M.[C_SUCU_EMPR]
		AND T.[C_ARTICULO] = M.[C_ARTICULO];     
	
--- DESDE MAESTRO DE ARTICULOS ---
	UPDATE T
	SET T.[N_ARTICULO] = A.[N_ARTICULO_FACT]

	FROM [dbo].[T_OC_DETALLE] T
	INNER JOIN [DIARCOP001].[DiarcoP].[dbo].[T050_ARTICULOS] A
		ON T.[C_ARTICULO] = A.[C_ARTICULO];   

---- ACTUALIZAR PENDIENTE DE ENTREGA
	UPDATE T
	SET ENTREGAS_PENDIENTES = (
		SELECT SUM(Sub.[PENDIENTE_UNIDADES] )
		FROM dbo.T_OC_DETALLE_EXT AS Sub
		WHERE Sub.C_ARTICULO = T.C_ARTICULO -- Mismo artículo
		  AND Sub.C_SUCU_DESTINO = T.C_SUCU_DESTINO -- Misma sucursal
		  AND Sub.F_EMISION < T.F_EMISION -- Fechas anteriores
		  AND Sub.M_CUMPLIDA_PARCIAL <> 'N' -- Excluir registros no cumplidos
	)
	FROM dbo.T_OC_DETALLE_EXT AS T
	WHERE T.M_CUMPLIDA_PARCIAL <> 'N'; -- Aplicar la condición para el registro actual
	
	--- DESDE TABLA DE STOCK DIARIO ---
	DECLARE @DIA INT;

-- Actualizar la tabla OC_DETALLE con el STOCK_UNIDADES
	UPDATE OC
	SET [STOCK_UNIDADES] = CASE
