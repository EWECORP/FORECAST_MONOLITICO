				CAST(M_OFERTA_DIA1  AS VARCHAR(1)) AS M_OFERTA_DIA1,  CAST(M_OFERTA_DIA2 AS VARCHAR(1))  AS M_OFERTA_DIA2, 
				CAST(M_OFERTA_DIA3  AS VARCHAR(1)) AS M_OFERTA_DIA3,  CAST(M_OFERTA_DIA4 AS VARCHAR(1))  AS M_OFERTA_DIA4, 
				CAST(M_OFERTA_DIA5  AS VARCHAR(1)) AS M_OFERTA_DIA5,  CAST(M_OFERTA_DIA6 AS VARCHAR(1))  AS M_OFERTA_DIA6, 
				CAST(M_OFERTA_DIA7  AS VARCHAR(1)) AS M_OFERTA_DIA7,  CAST(M_OFERTA_DIA8 AS VARCHAR(1))  AS M_OFERTA_DIA8, 
				CAST(M_OFERTA_DIA9  AS VARCHAR(1)) AS M_OFERTA_DIA9,  CAST(M_OFERTA_DIA10 AS VARCHAR(1)) AS M_OFERTA_DIA10, 
				CAST(M_OFERTA_DIA11 AS VARCHAR(1)) AS M_OFERTA_DIA11, CAST(M_OFERTA_DIA12 AS VARCHAR(1)) AS M_OFERTA_DIA12, 
				CAST(M_OFERTA_DIA13 AS VARCHAR(1)) AS M_OFERTA_DIA13, CAST(M_OFERTA_DIA14 AS VARCHAR(1)) AS M_OFERTA_DIA14, 
				CAST(M_OFERTA_DIA15 AS VARCHAR(1)) AS M_OFERTA_DIA15, CAST(M_OFERTA_DIA16 AS VARCHAR(1)) AS M_OFERTA_DIA16, 
				CAST(M_OFERTA_DIA17 AS VARCHAR(1)) AS M_OFERTA_DIA17, CAST(M_OFERTA_DIA18 AS VARCHAR(1)) AS M_OFERTA_DIA18, 
				CAST(M_OFERTA_DIA19 AS VARCHAR(1)) AS M_OFERTA_DIA19, CAST(M_OFERTA_DIA20 AS VARCHAR(1)) AS M_OFERTA_DIA20, 
				CAST(M_OFERTA_DIA21 AS VARCHAR(1)) AS M_OFERTA_DIA21, CAST(M_OFERTA_DIA22 AS VARCHAR(1)) AS M_OFERTA_DIA22, 
				CAST(M_OFERTA_DIA23 AS VARCHAR(1)) AS M_OFERTA_DIA23, CAST(M_OFERTA_DIA24 AS VARCHAR(1)) AS M_OFERTA_DIA24, 
				CAST(M_OFERTA_DIA25 AS VARCHAR(1)) AS M_OFERTA_DIA25, CAST(M_OFERTA_DIA26 AS VARCHAR(1)) AS M_OFERTA_DIA26, 
				CAST(M_OFERTA_DIA27 AS VARCHAR(1)) AS M_OFERTA_DIA27, CAST(M_OFERTA_DIA28 AS VARCHAR(1)) AS M_OFERTA_DIA28, 
				CAST(M_OFERTA_DIA29 AS VARCHAR(1)) AS M_OFERTA_DIA29, CAST(M_OFERTA_DIA30 AS VARCHAR(1)) AS M_OFERTA_DIA30, 
				CAST(M_OFERTA_DIA31 AS VARCHAR(1)) AS M_OFERTA_DIA31 
			FROM T710_ESTADIS_OFERTA_FOLDER
			WHERE C_SUCU_EMPR =	ISNULL(@C_SUCU_EMPR, C_SUCU_EMPR) 
				AND C_ARTICULO = ISNULL(@C_ARTICULO, C_ARTICULO) 
				AND ((C_ANIO = YEAR(@FechaOfFolDesde) AND C_MES = MONTH(@FechaOfFolDesde)) 
					OR (C_ANIO = YEAR(@FechaOfFolHasta) AND C_MES = MONTH(@FechaOfFolHasta))) 

			) E
		UNPIVOT
			(DIA FOR M_OFERTA_DIA IN (
			M_OFERTA_DIA1, M_OFERTA_DIA2, M_OFERTA_DIA3, M_OFERTA_DIA4, M_OFERTA_DIA5, M_OFERTA_DIA6, M_OFERTA_DIA7, M_OFERTA_DIA8, M_OFERTA_DIA9
			, M_OFERTA_DIA10, M_OFERTA_DIA11, M_OFERTA_DIA12, M_OFERTA_DIA13, M_OFERTA_DIA14, M_OFERTA_DIA15, M_OFERTA_DIA16, M_OFERTA_DIA17
			, M_OFERTA_DIA18, M_OFERTA_DIA19, M_OFERTA_DIA20, M_OFERTA_DIA21, M_OFERTA_DIA22, M_OFERTA_DIA23, M_OFERTA_DIA24, M_OFERTA_DIA25
			, M_OFERTA_DIA26, M_OFERTA_DIA27, M_OFERTA_DIA28, M_OFERTA_DIA29, M_OFERTA_DIA30, M_OFERTA_DIA31 ) 
			) OFERTA ON YEAR(FechaOfertaYFolder) = OFERTA.C_ANIO AND MONTH(FechaOfertaYFolder) = OFERTA.C_MES AND DAY(FechaOfertaYFolder) = SUBSTRING(OFERTA.M_OFERTA_DIA, 13, LEN(OFERTA.M_OFERTA_DIA) - 12)
		) AS X
	WHERE S_ELASTICIDAD_DETALLE.C_ARTICULO = X.C_ARTICULO
		AND S_ELASTICIDAD_DETALLE.C_SUCU_EMPR = X.C_SUCU_EMPR
		AND S_ELASTICIDAD_DETALLE.F_DIA= X.DateValue

	IF @@ERROR <> 0 
	    BEGIN
			SET @vchMensaje = 'Error en SQL < UPDATE en S_ELASTICIDAD_DETALLE (OFERTA) > en Stored Procedure SD99_CIERREGRAL_CALCULO_ESTADISTICA_DIAS_REPOSICION'
			RETURN 99
		END

	PRINT 'HAGO SEPA'
    

	--//////////////////////////////////// INSERTO Ventas //////////////////////////////////////////////////


	UPDATE S_ELASTICIDAD_DETALLE 
	SET Q_VENTA = X.Venta
	FROM (
		SELECT T.DateValue, 
			VENTA.C_ARTICULO, 
			VENTA.C_SUCU_EMPR, 
			VENTA.Venta
		FROM #TempFechas T
		LEFT JOIN (
			SELECT C_SUCU_EMPR, 
				C_ARTICULO, 
				SUBSTRING(Q_DIA, 6, LEN(Q_DIA) - 5) AS DIA, 
				Venta 
			FROM (
				SELECT C_SUCU_EMPR, C_ARTICULO, Q_DIA1, Q_DIA2, Q_DIA3, Q_DIA4, Q_DIA5, Q_DIA6, Q_DIA7, CAST(Q_DIA8 AS DECIMAL(11,3)) AS Q_DIA8, 
					Q_DIA9, Q_DIA10, Q_DIA11, Q_DIA12, Q_DIA13, Q_DIA14, Q_DIA15, Q_DIA16, Q_DIA17, Q_DIA18, Q_DIA19, Q_DIA20, Q_DIA21, Q_DIA22, 
					Q_DIA23, Q_DIA24, Q_DIA25, Q_DIA26, Q_DIA27, Q_DIA28, Q_DIA29, Q_DIA30, Q_DIA31 
				FROM T710_ESTADIS 
				WHERE C_SUCU_EMPR = ISNULL(@C_SUCU_EMPR, C_SUCU_EMPR)
					AND C_ARTICULO = ISNULL(@C_ARTICULO, C_ARTICULO)
				) E
				UNPIVOT
					(Venta FOR Q_DIA IN (
					Q_DIA1, Q_DIA2, Q_DIA3, Q_DIA4, Q_DIA5, Q_DIA6, Q_DIA7, Q_DIA8, Q_DIA9, Q_DIA10, Q_DIA11, Q_DIA12, Q_DIA13, Q_DIA14, Q_DIA15, 
					Q_DIA16, Q_DIA17, Q_DIA18, Q_DIA19, Q_DIA20, Q_DIA21, Q_DIA22, Q_DIA23, Q_DIA24, Q_DIA25, Q_DIA26, Q_DIA27, Q_DIA28, Q_DIA29, 
					Q_DIA30, Q_DIA31 )
					) U 
				) VENTA ON DAY(T.DateValue) = VENTA.DIA
		) AS X
	WHERE S_ELASTICIDAD_DETALLE.C_ARTICULO = X.C_ARTICULO
		AND S_ELASTICIDAD_DETALLE.C_SUCU_EMPR = X.C_SUCU_EMPR
		AND S_ELASTICIDAD_DETALLE.F_DIA = X.DateValue


	IF @@ERROR <> 0 
	    BEGIN
			SET @vchMensaje = 'Error en SQL < UPDATE en S_ELASTICIDAD_DETALLE (VENTA) > en Stored Procedure SD99_CIERREGRAL_CALCULO_ESTADISTICA_DIAS_REPOSICION'
			RETURN 99
		END

	--/////////////////////////////////// INSERTO STOCK ////////////////////////////////////////////////


	UPDATE S_ELASTICIDAD_DETALLE 
	SET Q_STOCK = X.STOCK 
	FROM (
		SELECT T.DateValue,
			C_SUCU_EMPR, 
			C_ARTICULO, 
			STOCK
		FROM #TempFechas T
		INNER JOIN (
			SELECT C_SUCU_EMPR, C_ARTICULO, C_ANIO, C_MES, Q_DIA1, Q_DIA2, Q_DIA3, Q_DIA4, Q_DIA5, Q_DIA6, Q_DIA7, 
				CAST(Q_DIA8 AS DECIMAL(11,3)) AS Q_DIA8, Q_DIA9, Q_DIA10, Q_DIA11, Q_DIA12, Q_DIA13, Q_DIA14, Q_DIA15, 
				Q_DIA16, Q_DIA17, Q_DIA18, Q_DIA19, Q_DIA20, Q_DIA21, Q_DIA22, Q_DIA23, Q_DIA24, Q_DIA25, Q_DIA26, 
				Q_DIA27, Q_DIA28, Q_DIA29, Q_DIA30, Q_DIA31 
			FROM T710_ESTADIS_STOCK
			WHERE C_SUCU_EMPR = ISNULL(@C_SUCU_EMPR, C_SUCU_EMPR) 
				AND C_ARTICULO = ISNULL(@C_ARTICULO, C_ARTICULO) 
				AND ((C_ANIO = YEAR(@FechaStockDesde) AND C_MES = MONTH(@FechaStockDesde)) 
					OR (C_ANIO = YEAR(@FechaStockHasta) AND C_MES = MONTH(@FechaStockHasta))) 
				) E
			UNPIVOT
				(STOCK FOR Q_DIA IN (
				Q_DIA1, Q_DIA2, Q_DIA3, Q_DIA4, Q_DIA5, Q_DIA6, Q_DIA7, Q_DIA8, Q_DIA9, Q_DIA10, Q_DIA11, Q_DIA12, Q_DIA13, Q_DIA14, Q_DIA15, 
				Q_DIA16, Q_DIA17, Q_DIA18, Q_DIA19, Q_DIA20, Q_DIA21, Q_DIA22, Q_DIA23, Q_DIA24, Q_DIA25, Q_DIA26, Q_DIA27, Q_DIA28, Q_DIA29, 
				Q_DIA30, Q_DIA31 )
				) ZZ ON YEAR(FechaStock) = ZZ.C_ANIO AND MONTH(FechaStock) = ZZ.C_MES AND DAY(FechaStock) = SUBSTRING(Q_DIA, 6, LEN(Q_DIA) - 5) 
		) AS X
	WHERE S_ELASTICIDAD_DETALLE.C_ARTICULO = X.C_ARTICULO
		AND S_ELASTICIDAD_DETALLE.C_SUCU_EMPR = X.C_SUCU_EMPR
		AND S_ELASTICIDAD_DETALLE.F_DIA = X.DateValue
		


	IF @@ERROR <> 0 
	    BEGIN
			SET @vchMensaje = 'Error en SQL < UPDATE en S_ELASTICIDAD_DETALLE (STOCK) > en Stored Procedure SD99_CIERREGRAL_CALCULO_ESTADISTICA_DIAS_REPOSICION'
			RETURN 99
		END

--//////  INSERTO SEPA
	UPDATE S_ELASTICIDAD_DETALLE 
	SET M_SEPA = X.M_SEPA
	FROM (
		SELECT T.DateValue,
			T.FechaOfertaYFolder, 
			SEPA.C_SUCU_EMPR, 
			SEPA.C_ARTICULO, 
			SEPA.DIA AS M_SEPA
		FROM
		#TempFechas T
		INNER JOIN 
			(
			SELECT C_ANIO, 
				C_MES, 
				C_SUCU_EMPR, 
				C_ARTICULO, 
				CAST(M_SEPA_DIA1  AS VARCHAR(1)) AS M_SEPA_DIA1,  CAST(M_SEPA_DIA2 AS VARCHAR(1))  AS M_SEPA_DIA2, 
				CAST(M_SEPA_DIA3  AS VARCHAR(1)) AS M_SEPA_DIA3,  CAST(M_SEPA_DIA4 AS VARCHAR(1))  AS M_SEPA_DIA4, 
				CAST(M_SEPA_DIA5  AS VARCHAR(1)) AS M_SEPA_DIA5,  CAST(M_SEPA_DIA6 AS VARCHAR(1))  AS M_SEPA_DIA6, 
				CAST(M_SEPA_DIA7  AS VARCHAR(1)) AS M_SEPA_DIA7,  CAST(M_SEPA_DIA8 AS VARCHAR(1))  AS M_SEPA_DIA8, 
				CAST(M_SEPA_DIA9  AS VARCHAR(1)) AS M_SEPA_DIA9,  CAST(M_SEPA_DIA10 AS VARCHAR(1)) AS M_SEPA_DIA10, 
				CAST(M_SEPA_DIA11 AS VARCHAR(1)) AS M_SEPA_DIA11, CAST(M_SEPA_DIA12 AS VARCHAR(1)) AS M_SEPA_DIA12, 
				CAST(M_SEPA_DIA13 AS VARCHAR(1)) AS M_SEPA_DIA13, CAST(M_SEPA_DIA14 AS VARCHAR(1)) AS M_SEPA_DIA14, 
				CAST(M_SEPA_DIA15 AS VARCHAR(1)) AS M_SEPA_DIA15, CAST(M_SEPA_DIA16 AS VARCHAR(1)) AS M_SEPA_DIA16, 
				CAST(M_SEPA_DIA17 AS VARCHAR(1)) AS M_SEPA_DIA17, CAST(M_SEPA_DIA18 AS VARCHAR(1)) AS M_SEPA_DIA18, 
				CAST(M_SEPA_DIA19 AS VARCHAR(1)) AS M_SEPA_DIA19, CAST(M_SEPA_DIA20 AS VARCHAR(1)) AS M_SEPA_DIA20, 
				CAST(M_SEPA_DIA21 AS VARCHAR(1)) AS M_SEPA_DIA21, CAST(M_SEPA_DIA22 AS VARCHAR(1)) AS M_SEPA_DIA22, 
