	SET NOCOUNT ON;
	SELECT 'COD_FAMILIA','COD_PRODUTO'
	union all
	SELECT	
	distinct cast(C_CLASIFICACION_COMPRA as varchar(10)),
	cast(C_ARTICULO as varchar(10))
