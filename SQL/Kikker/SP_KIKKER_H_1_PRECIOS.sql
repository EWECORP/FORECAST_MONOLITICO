I_PRECIO_VTA_31 as I_PRECIO_VTA_31
from  [DIARCOP001].[DIARCOP].[dbo].[T710_ESTADIS_PRECIOS]
where  C_ARTICULO in (select C_ARTICULO from ##surtido) and C_ANIO=@anio AND C_MES=@mes and (I_PRECIO_VTA_1 + I_PRECIO_VTA_2 + I_PRECIO_VTA_3 + I_PRECIO_VTA_4 + I_PRECIO_VTA_5 + I_PRECIO_VTA_6 + I_PRECIO_VTA_7 +
I_PRECIO_VTA_8 + I_PRECIO_VTA_9 + I_PRECIO_VTA_10 + I_PRECIO_VTA_11 + I_PRECIO_VTA_12 + I_PRECIO_VTA_13 + I_PRECIO_VTA_14 +I_PRECIO_VTA_15 + I_PRECIO_VTA_16 +
