L_PRECIOS
into ##precios_historicos
FROM   
(SELECT 
C_ANIO,
C_MES, 
C_ARTICULO,
case C_SUCU_EMPR when 41 then '41CD' else DBO.[NORMALIZA_STRING](C_SUCU_EMPR) end as C_SUCU_EMPR,
I_PRECIO_VTA_1 as I_PRECIO_VTA_1,
I_PRECIO_VTA_2 as I_PRECIO_VTA_2,
