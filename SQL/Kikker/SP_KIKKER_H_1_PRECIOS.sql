) as PRECIOS
UNPIVOT  
(L_PRECIOS FOR C_DIA IN (I_PRECIO_VTA_1,I_PRECIO_VTA_2,I_PRECIO_VTA_3,I_PRECIO_VTA_4,I_PRECIO_VTA_5,I_PRECIO_VTA_6,I_PRECIO_VTA_7,I_PRECIO_VTA_8,I_PRECIO_VTA_9,I_PRECIO_VTA_10,
I_PRECIO_VTA_11,I_PRECIO_VTA_12,I_PRECIO_VTA_13,I_PRECIO_VTA_14,I_PRECIO_VTA_15,I_PRECIO_VTA_16,I_PRECIO_VTA_17,I_PRECIO_VTA_18,I_PRECIO_VTA_19,I_PRECIO_VTA_20,
I_PRECIO_VTA_21,I_PRECIO_VTA_22,I_PRECIO_VTA_23,I_PRECIO_VTA_24,I_PRECIO_VTA_25,I_PRECIO_VTA_26,I_PRECIO_VTA_27,I_PRECIO_VTA_28,I_PRECIO_VTA_29,I_PRECIO_VTA_30,I_PRECIO_VTA_31)
